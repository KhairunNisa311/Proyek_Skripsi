<h2>FUZZY ANALYTICAL HIERARCY PROCESS (FAHP)</h2>
<p>Sistem ini merupakan sistem pendukung keputusan yang menggunakan metode Fuzzy AHP untuk menentukan 
perangkingan skala prioritas dalam menentukan pemenuhan sarana dan prasarana destinasi wisata di Kabupaten Sumenep. 
Sistem ini diharapkan dapat membantu pemerintah dalam merumuskan kebijakan yang tepat, 
memungkinkan pengukuran skala prioritas kebutuhan sarana dan prasarana destinasi wisata di Kabupaten Sumenep dengan lebih efektif dan efisien </p>
<p>Pada sistem ini juga terdapat langkah langkah proses perhitungan metode Fuzzy AHP. Langkah-langkah ini dilakukan untuk memastikan bahwa 
pengambilan keputusan terkait kebijakan infrastruktur wisata dilakukan dengan akurat dan efisien</p>

